    private sealed class Utf8Encoding : ICharset {
    /// <summary>Not documented yet.</summary>
    /// <param name='transform'>An ITransform object.</param>
    /// <returns>A string object.</returns>
      public string GetString(ITransform transform) {
        StringBuilder builder = new StringBuilder();
        ReadUtf8(transform, -1, builder, true);
        return builder.ToString();
      }

      private static int ReadUtf8(
        ITransform stream,
        int bytesCount,
        StringBuilder builder,
        bool replace) {
        if (stream == null) {
          throw new ArgumentNullException("stream");
        }
        if (builder == null) {
          throw new ArgumentNullException("builder");
        }
        int cp = 0;
        int bytesSeen = 0;
        int bytesNeeded = 0;
        int lower = 0x80;
        int upper = 0xbf;
        int pointer = 0;
        while (pointer < bytesCount || bytesCount < 0) {
          int b = stream.ReadByte();
          if (b < 0) {
            if (bytesNeeded != 0) {
              bytesNeeded = 0;
              if (replace) {
                builder.Append((char)0xfffd);
                if (bytesCount >= 0) {
                  return -2;
                }
                break;  // end of stream
              }
              return -1;
            } else {
              if (bytesCount >= 0) {
                return -2;
              }
              break;  // end of stream
            }
          }
          if (bytesCount > 0) {
            ++pointer;
          }
          if (bytesNeeded == 0) {
            if ((b & 0x7f) == b) {
              builder.Append((char)b);
            } else if (b >= 0xc2 && b <= 0xdf) {
              bytesNeeded = 1;
              cp = (b - 0xc0) << 6;
            } else if (b >= 0xe0 && b <= 0xef) {
              lower = (b == 0xe0) ? 0xa0 : 0x80;
              upper = (b == 0xed) ? 0x9f : 0xbf;
              bytesNeeded = 2;
              cp = (b - 0xe0) << 12;
            } else if (b >= 0xf0 && b <= 0xf4) {
              lower = (b == 0xf0) ? 0x90 : 0x80;
              upper = (b == 0xf4) ? 0x8f : 0xbf;
              bytesNeeded = 3;
              cp = (b - 0xf0) << 18;
            } else {
              if (replace) {
                builder.Append((char)0xfffd);
              } else {
                return -1;
              }
            }
            continue;
          } else if (b < lower || b > upper) {
            cp = bytesNeeded = bytesSeen = 0;
            lower = 0x80;
            upper = 0xbf;
            if (replace) {
              builder.Append((char)0xfffd);
              // "Read" the last byte again
              if (b < 0x80) {
                builder.Append((char)b);
              } else if (b >= 0xc2 && b <= 0xdf) {
                bytesNeeded = 1;
                cp = (b - 0xc0) << 6;
              } else if (b >= 0xe0 && b <= 0xef) {
                lower = (b == 0xe0) ? 0xa0 : 0x80;
                upper = (b == 0xed) ? 0x9f : 0xbf;
                bytesNeeded = 2;
                cp = (b - 0xe0) << 12;
              } else if (b >= 0xf0 && b <= 0xf4) {
                lower = (b == 0xf0) ? 0x90 : 0x80;
                upper = (b == 0xf4) ? 0x8f : 0xbf;
                bytesNeeded = 3;
                cp = (b - 0xf0) << 18;
              } else {
                builder.Append((char)0xfffd);
              }
              continue;
            } else {
              return -1;
            }
          } else {
            lower = 0x80;
            upper = 0xbf;
            ++bytesSeen;
            cp += (b - 0x80) << (6 * (bytesNeeded - bytesSeen));
            if (bytesSeen != bytesNeeded) {
              continue;
            }
            int ret = cp;
            cp = 0;
            bytesSeen = 0;
            bytesNeeded = 0;
            if (ret <= 0xffff) {
              builder.Append((char)ret);
            } else {
              int ch = ret - 0x10000;
              int lead = (ch / 0x400) + 0xd800;
              int trail = (ch & 0x3ff) + 0xdc00;
              builder.Append((char)lead);
              builder.Append((char)trail);
            }
          }
        }
        if (bytesNeeded != 0) {
          if (replace) {
            builder.Append((char)0xfffd);
          } else {
            return -1;
          }
        }
        return 0;
      }
    }

    private sealed class SingleByteEncoding : ICharset {
      private int[] map;

      public SingleByteEncoding(int[] map) {
        this.map = map;
      }

    /// <summary>Not documented yet.</summary>
    /// <param name='transform'>An ITransform object.</param>
    /// <returns>A string object.</returns>
      public string GetString(ITransform transform) {
        StringBuilder builder = new StringBuilder();
        while (true) {
          int b = transform.ReadByte();
          if (b < 0) {
            break;
          }
          if (b < 0x80) {
            builder.Append((char)b);
          } else {
            builder.Append((char)this.map[b & 0x7f]);
          }
        }
        return builder.ToString();
      }
    }

